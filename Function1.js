'use strict'

function twoParameters(num1, num2) {
    return num1 - num2;

}

console.log(twoParameters(10,2));

function multiple(welcome,name,age,gender){
    return `${welcome} my name is ${name}, I am ${age} years old and of gender ${gender}.`
}

console.log(multiple("Hi","Jack","22","Male"))

powerUP = (n1, n2) => Math.pow(n1,n2);

console.log(powerUp(2,3));
